# rumus persegi panjang p * l

print("\n1.luas persegi panjang")
p = int(input("masukkan nilai p: "))
l = int(input("masukkan nilai l: "))
luas_pp = p * l

print("luas persegi panjang adalah", luas_pp)

# rumus belah keupat 1/2 * d1 * d2

print("\n2.luas belah ketupat")
d1 = int(input("masukkan nilai d1: "))
